''' Д/з №2 
    применение операторов '''


exponentiation = 5**5
print ('5 to the 5th =', exponentiation) #оператор возведения в степень

addition = -5+5
print('-5+5 =', addition) #оператор сложения, отрицательное и положительное число

subtraction = -10-17
print('-10-17 =', subtraction) #оператор вычитания, отрицательные числа

multiplication = 12*4
print('12*4 =', multiplication) #оператор умножения

division = 25/20
print('25/20 =', division) #оператор деления

integer_division = 15//4
print('the floor of the division 15/4 =', integer_division) #оператор деления без остатка

number_comparison1 = 12>3
print('12>3 is it true?', number_comparison1) #оператор сравнения

number_comparison2 = 0==3 and 8>10
print('0=3 and 8>10 is it true?', number_comparison2) #оператор сравнения, тождественности, and

number_comparison3 = 15+3>5*3
print('15+3>5*3 is it true?', number_comparison3) #оператор сравнения, сложения, умножения

numbers = [2, 10, 31, 57, 80]
print(numbers, 'is it 10 in numbers?', 10 in numbers)
print(numbers, '5 is not in numbers.', 5 not in numbers) #операторы in, not in
